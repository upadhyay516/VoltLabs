// Minimal service worker. Its main job is just to *exist* — Chrome/Android
// require a registered service worker with a fetch handler before they'll
// show the "Install app" prompt. On top of that, it caches a few core pages
// so the app shell still loads (with a friendly offline message for
// anything not cached) if someone opens it with no signal.

const CACHE_NAME = "voltlab-shell-v1";
const PRECACHE_URLS = ["/", "/products", "/icon.svg"];

self.addEventListener("install", (event) => {
  event.waitUntil(
    caches.open(CACHE_NAME).then((cache) => cache.addAll(PRECACHE_URLS))
  );
  self.skipWaiting();
});

self.addEventListener("activate", (event) => {
  event.waitUntil(
    caches
      .keys()
      .then((keys) =>
        Promise.all(
          keys.filter((k) => k !== CACHE_NAME).map((k) => caches.delete(k))
        )
      )
  );
  self.clients.claim();
});

self.addEventListener("fetch", (event) => {
  // Only handle simple GET navigations/static assets; let API calls,
  // Supabase, and Razorpay requests pass straight through untouched.
  if (event.request.method !== "GET") return;

  const url = new URL(event.request.url);
  if (url.origin !== self.location.origin) return;
  if (url.pathname.startsWith("/api/")) return;

  event.respondWith(
    caches.match(event.request).then((cached) => {
      const network = fetch(event.request)
        .then((response) => {
          if (response.ok) {
            const clone = response.clone();
            caches.open(CACHE_NAME).then((cache) => cache.put(event.request, clone));
          }
          return response;
        })
        .catch(() => cached);
      return cached || network;
    })
  );
});
